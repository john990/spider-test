public class Xinli001Post {
	private String id = "";
	private String title;
	private String content;
	private String date;
	private String tag;
	private String images;
	private String from_web;
	private String from_link;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getTag() {
		return tag;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}

	public String getImages() {
		return images;
	}

	public void setImages(String images) {
		this.images = images;
	}

	public String getFrom_web() {
		return from_web;
	}

	public void setFrom_web(String from_web) {
		this.from_web = from_web;
	}

	public String getFrom_link() {
		return from_link;
	}

	public void setFrom_link(String from_link) {
		this.from_link = from_link;
	}
}
